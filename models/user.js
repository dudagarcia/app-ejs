class User{
    constructor(id, name, birthDate, age, email, phoneNumber, section, role) {
        this.id = id;
        this.name = name;
        this.birthDate = birthDate;
        this.age = age;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.section = section;
        this.role = role;
    }
}

export default User;